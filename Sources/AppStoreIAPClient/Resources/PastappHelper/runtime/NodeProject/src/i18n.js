function pickLang(raw) {
    const v = String(raw || '').trim();
    if (!v) return 'zh-Hans';
    const lower = v.toLowerCase();
    if (lower.startsWith('zh-hk') || lower === 'zh_hk') return 'zh-HK';
    if (lower.startsWith('zh-hant') || lower.startsWith('zh-tw') || lower === 'zh_tw') return 'zh-Hant';
    if (lower.startsWith('zh-hans') || lower.startsWith('zh-cn') || lower === 'zh' || lower === 'zh_cn') return 'zh-Hans';
    if (lower.startsWith('ja')) return 'ja';
    if (lower.startsWith('ko')) return 'ko';
    if (lower.startsWith('th')) return 'th';
    if (lower.startsWith('en')) return 'en';
    return 'zh-Hans';
}

const LANG = pickLang(process.env.IPA_LANG);

export function t(key, params = {}) {
    const table = MESSAGES[LANG] || MESSAGES['zh-Hans'];
    let template = table[key];
    if (template === undefined) template = MESSAGES['zh-Hans'][key];
    if (template === undefined) return key;
    return template.replace(/\{(\w+)\}/g, (match, name) => (
        Object.prototype.hasOwnProperty.call(params, name) ? String(params[name]) : match
    ));
}

export function currentLang() {
    return LANG;
}
